-- ----------------------------
-- Function structure for AVG_RATING
-- ----------------------------
CREATE OR REPLACE
FUNCTION "C##CHALDAL"."AVG_RATING" AS
BEGIN

	total :=0;
	FOR rev IN (SELECT RATING FROM REVIEW WHERE PRODUCT_ID=PROD_ID)
	LOOP
		total := total + rev.RATING;
	
	END LOOP;
	
	SELECT COUNT(*) INTO rev_no
	FROM REVIEW 
	WHERE PRODUCT_ID=PROD_ID;
	
	avgRating := total/rev_no;
	
	RETURN avgRating;
	
EXCEPTION
	WHEN ZERO_DIVIDE THEN
		RETURN 0;
	WHEN OTHERS THEN
		RETURN 0;

END;
