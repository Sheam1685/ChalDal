CREATE TRIGGER "C##CHALDAL"."SELLER_BALANCE_DEDUCTION" AFTER UPDATE OF "APPROVAL_STATUS" ON "C##CHALDAL"."RETURN_ORDER" REFERENCING OLD AS "OLD" NEW AS "NEW" FOR EACH ROW 
DECLARE

	ORD_ID NUMBER;
	QUANT NUMBER;
	PROD_ID NUMBER;
	PROD_PRICE NUMBER;
	SLR_ID NUMBER;
	TOT_PRICE NUMBER;
	OLD_BALANCE NUMBER;
	NEW_BALANCE NUMBER;

BEGIN

	ORD_ID := :OLD.ORDER_ID;
	
	SELECT COUNT(*) INTO QUANT
	FROM ORDERED_ITEMS
	WHERE ORDER_ID = ORD_ID;
	
	SELECT MAX(PRODUCT_ID) INTO PROD_ID
	FROM ORDERED_ITEMS
	WHERE ORDER_ID = ORD_ID;
	
	SELECT PRICE, SELLER_ID INTO PROD_PRICE, SLR_ID
	FROM PRODUCT
	WHERE PRODUCT_ID = PROD_ID;
	
	SELECT BALANCE INTO OLD_BALANCE
	FROM SELLER
	WHERE SELLER_ID = SLR_ID;
	
	TOT_PRICE := PROD_PRICE*QUANT;
	NEW_BALANCE := OLD_BALANCE - TOT_PRICE;
	
	UPDATE SELLER SET BALANCE=NEW_BALANCE WHERE SELLER_ID = SLR_ID;
	

END;
